#  Write a lambda to add two numbers.
add = lambda a,b: a+b
print(add(1,2))

# Write a lambda to find square of a number.
square_number = lambda x: x*x
print(square_number(9))

# Write a lambda to check if a number is even.
even_number = lambda x: "even" if x%2 == 0 else "odd"
print(even_number(4))
print(even_number(7))

